#include "libft.h"

char	*ft_strrev(char *s)
{
	char	tmp;
	char	*s0;
	char	*s1;

	s0 = s - 1;
	s1 = s;
	while (*s1)
		s1++;
	while (s1-- > ++s0)
	{
		tmp = *s0;
		*s0 = *s1;
		*s1 = tmp;
	}
	return (s);
}
